    <div class='nbody'>
1- Que sont les cookies ?
<br />
- Les cookies sont de petits fichiers texte stockés sur votre ordinateur, sur un téléphone portable ou sur un ordinateur portable. Ils contiennent un identifiant (chaîne de lettres et de chiffres) envoyé par un serveur Web à un navigateur Web et stocké. Par le navigateur. L'ID est ensuite renvoyé au serveur à chaque fois que le navigateur demande une page au serveur et porte généralement le nom du site d'où il provient.
<br /><br />
2- Pour Quelles Raisons Utilisons-nous Les Cookies ?
<br />
Nous utilisons les cookies pour faciliter l’utilisation de nos sites et mieux personnaliser les sites et nos produits en fonction de vos intérêts et de vos besoins. Les cookies peuvent le faire car les sites web peuvent lire et écrire ces fichiers, ce qui leur permet de vous reconnaitre et de se souvenir d’informations importantes, rendant votre utilisation d’un site web plus pratique (par exemple, en se souvenant de vos préférences).
<br />
Les cookies peuvent également être utilisés pour accélérer vos futures activités et vos prochaines visites de nos sites. Nous utilisons aussi les cookies pour compiler des statistiques agrégées anonymes qui nous permettent de comprendre la façon dont les utilisateurs utilisent nos Sites et nous aident à améliorer la structure et le contenu de nos Sites.
<br /><br />
3- Quels Types De Cookies Utilisons-nous ?
<br />
Deux types de cookies peuvent être utilisés sur les Sites : les « cookies de session » et les « cookies permanents ». Les cookies de session sont des cookies temporaires qui restent sur votre appareil jusqu’à ce que vous quittiez le Site. Un cookie permanent reste sur votre appareil pour une durée bien plus longue ou jusqu’à ce que vous le supprimiez manuellement (la durée pendant laquelle le cookie reste sur votre appareil dépend de la durée ou « durée de vie » du cookie en question).
<br />
- Cookies de tiers :
<br />
Nous avons recours à un certain nombre de fournisseurs qui peuvent également placer des cookies sur votre appareil pour notre compte lorsque vous visitez nos sites pour leur permettre de fournir leurs services. Pour plus d’information sur ces cookies, et sur la façon de les refuser (opt-out), veuillez consulter leur charte de protection des données à caractère personnel.


<br /><br />
4- Comment Gerer Les Cookies ?
<br />
Bien que la plupart des navigateurs internet soient initialement paramétrés pour accepter automatiquement les cookies, la plupart des navigateurs internet vous permettent de modifier les paramètres pour bloquer les cookies ou vous alerter lorsque des cookies sont envoyés sur votre appareil.
<br />
En sus des options prévues ci-dessus, vous pouvez refuser, accepter ou supprimer les cookies du site à tout moment en activant ou en accédant aux paramètres de votre navigateur. Des informations sur la procédure à suivre pour activer, désactiver ou supprimer les cookies sont disponibles sur le site web du fournisseur de votre navigateur Internet via le menu d’aide.
<br />
Vous devez savoir que si des cookies sont désactivés ou supprimés, toutes les fonctionnalités du site pourront ne pas fonctionner normalement, par exemple vous pourrez être dans l’incapacité de visiter certaines parties de notre Site ou de recevoir des informations personnalisées lorsque vous visitez le Site.
<br />
Si vous utilisez plusieurs appareils pour consulter les Sites et y accéder (par exemple, votre ordinateur, votre smartphone, votre tablette, etc.), vous devez vous assurer que le navigateur sur chacun des appareils est paramétré pour correspondre à vos préférences en termes de cookies.


<br />
- Les informations que nous collectons peuvent être stockées, traitées et transférées entre tous les pays dans lesquels nous opérons afin de nous permettre d'utiliser les informations en conformité avec cette politique.<br />
- Les informations que nous collectons peuvent être transférées aux pays suivants qui ne disposent pas de lois en matière de protection des données équivalentes à celles en vigueur dans l'Espace économique européen: États-Unis d'Amérique, Russie, Japon, Chine et Inde.<br />
- Les informations personnelles que vous publiez sur notre site Web ou que vous soumettez pour publication sur notre site Web peuvent être disponibles, via Internet, dans le monde entier. Nous ne pouvons empêcher l'utilisation ou la mauvaise utilisation de ces informations par des tiers.<br />
Vous acceptez expressément les transferts d'informations personnelles décrits dans cette section.
<br /><br />
5- Conservation des informations personnelles :
<br />
Cette section présente nos politiques et procédures de conservation des données, conçues pour nous aider à nous conformer à nos obligations légales en matière de conservation et de suppression des informations personnelles.<br />

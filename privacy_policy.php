	<div class='ntit'>Politique de confidentialité Toubkal Atlas</div>
    
    <div class='nbody'>
La confidentialité des visiteurs de notre site Web est très importante pour nous et nous nous engageons à la protéger. Cette politique explique ce que nous ferons avec vos informations personnelles. Consentir à notre utilisation des cookies conformément aux termes de cette politique lorsque vous visitez notre site Web pour la première fois, nous permet d'utiliser des cookies à chaque fois que vous visitez notre site Web.
<br /><br />
1- Collecte d'informations personnelles :
<br />
Les types d'informations personnelles suivants peuvent être collectés, stockés et utilisés :
<br />
- des informations sur votre ordinateur, notamment votre adresse IP, votre localisation géographique, le type et la version du navigateur, ainsi que le système d'exploitation.<br />
- des informations sur vos visites et votre utilisation de ce site Web, y compris la source de parrainage, la durée de la visite, les pages vues et les chemins de navigation du site Web.<br />
- des informations, telles que votre adresse e-mail, que vous entrez lors de votre inscription sur notre site Web.<br />
- les informations que vous saisissez lorsque vous créez un profil sur notre site Web, par exemple votre nom, vos photos de profil, votre sexe, votre anniversaire, le statut de votre relation, vos intérêts et loisirs, des informations sur votre formation et votre emploi.<br />
- des informations, telles que votre nom et votre adresse e-mail, que vous entrez afin de configurer des abonnements à nos e-mails et / ou newsletters.<br />
- les informations que vous entrez lors de l'utilisation des services sur notre site.<br />
- les informations générées lors de l'utilisation de notre site Web, y compris quand, à quelle fréquence et dans quelles circonstances vous les utilisez.<br />
- les informations relatives à tout ce que vous achetez, aux services que vous utilisez ou aux transactions effectuées via notre site Web, notamment vos nom, adresse, numéro de téléphone, adresse électronique et coordonnées de carte de crédit.<br />
- les informations que vous publiez sur notre site Web dans l'intention de les publier sur Internet, y compris votre nom d'utilisateur, vos photos de profil et le contenu de vos publications.<br />
- les informations contenues dans toute communication que vous nous envoyez par courrier électronique ou par le biais de notre site Web, y compris son contenu et ses métadonnées de communication.<br />
- toute autre information personnelle que vous nous envoyez.<br />
<br />
Avant de nous divulguer les informations personnelles d'une autre personne, vous devez obtenir le consentement de cette personne pour la divulgation et le traitement de ces informations conformément à la présente politique.
<br /><br />
2- Utilisation de vos informations personnelles :
<br />
Les informations personnelles qui nous sont fournies via notre site Web seront utilisées aux fins spécifiées dans la présente politique ou sur les pages correspondantes du site.<br />
Nous pouvons utiliser vos informations personnelles pour :
<br />
- administrer notre site Web et notre entreprise.<br />
- personnaliser notre site pour vous.<br />
- permettant votre utilisation des services disponibles sur notre site.<br />
- vous envoyer des biens achetés via notre site Web.<br />
- fournir des services achetés via notre site Web.<br />
- vous envoyer des relevés, des factures et des rappels de paiement, et collecter vos paiements.<br />
- vous envoyer des communications commerciales non marketing.<br />
- vous envoyer des notifications par courrier électronique que vous avez spécifiquement demandées.<br />
- vous envoyer notre lettre d'information par courrier électronique, si vous en avez fait la demande (vous pouvez nous informer à tout moment si vous n'avez plus besoin de la lettre d'information).<br />
- en vous envoyant des communications marketing relatives à notre activité ou aux activités de tiers soigneusement sélectionnés que nous pensons pouvoir vous intéresser, par la poste ou, si vous y consentez expressément, par courrier électronique ou par une technologie similaire (vous pouvez nous en informer) à tout moment si vous n’avez plus besoin de communication marketing).<br />
- fournir à des tiers des informations statistiques sur nos utilisateurs (mais ces tiers ne seront pas en mesure d'identifier un utilisateur individuel à partir de ces informations).<br />
- traiter les demandes de renseignements et les plaintes formulées par vous ou à votre sujet concernant notre site Web.
- sécuriser notre site Web et éviter les fraudes.<br />
- vérification du respect des conditions d'utilisation de notre site Web (y compris la surveillance des messages privés envoyés via le service de messagerie privée de notre site Web). et autres utilisations.<br />
- Si vous soumettez des informations personnelles pour publication sur notre site Web, nous les publierons ou les utiliserons conformément à la licence que vous nous avez concédée.<br />
<br />
* Vos paramètres de confidentialité peuvent être utilisés pour limiter la publication de vos informations sur notre site Web et peuvent être ajustés à l'aide des contrôles de confidentialité sur le site Web.
<br />
* Nous ne fournirons pas vos informations personnelles à des tiers sans votre consentement exprès pour leur marketing direct ou celui de tout autre tiers.
<br /><br />
3- Divulgation d'informations personnelles :
<br />
Nous pouvons divulguer vos informations personnelles à l'un de nos employés, dirigeants, assureurs, conseillers professionnels, agents, fournisseurs ou sous-traitants, dans la mesure raisonnablement nécessaire aux fins énoncées dans la présente politique.
<br />
Nous pouvons divulguer vos informations personnelles à tout membre de notre groupe de sociétés (c'est-à-dire nos filiales, notre société de portefeuille ultime et toutes ses filiales) dans la mesure raisonnablement nécessaire aux fins énoncées dans la présente politique.
<br /><br />
- Nous pouvons divulguer vos informations personnelles dans la mesure où la loi l’y oblige. dans le cadre de procédures judiciaires en cours ou à venir. et afin d'établir, d'exercer ou de défendre nos droits légaux (y compris la fourniture d'informations à des tiers à des fins de prévention de la fraude et de réduction du risque de crédit). à l'acheteur (ou à l'acheteur éventuel) de toute entreprise ou de tout élément d'actif que nous vendons (ou envisageons) de vendre, ainsi qu'à toute personne qui, selon nous, peut raisonnablement demander à un tribunal ou à une autre autorité compétente la divulgation de ces informations lorsque: opinion raisonnable, ce tribunal ou cette autorité serait raisonnablement susceptible d’ordonner la divulgation de ces renseignements personnels. Sauf indication contraire dans la présente politique, nous ne communiquerons pas vos informations personnelles à des tiers.
